package com.homw.gateway.entity.dto;

public class DeviceDataPoint {

	private String outerNo;
	private String yesdayPoint;
	private String nowdayPoint;
	private String curUsePoint;
	private String otherUsePoint;
	
	public String getOuterNo() {
		return outerNo;
	}
	public void setOuterNo(String outerNo) {
		this.outerNo = outerNo;
	}
	public String getYesdayPoint() {
		return yesdayPoint;
	}
	public void setYesdayPoint(String yesdayPoint) {
		this.yesdayPoint = yesdayPoint;
	}
	public String getNowdayPoint() {
		return nowdayPoint;
	}
	public void setNowdayPoint(String nowdayPoint) {
		this.nowdayPoint = nowdayPoint;
	}
	public String getCurUsePoint() {
		return curUsePoint;
	}
	public void setCurUsePoint(String curUsePoint) {
		this.curUsePoint = curUsePoint;
	}
	public String getOtherUsePoint() {
		return otherUsePoint;
	}
	public void setOtherUsePoint(String otherUsePoint) {
		this.otherUsePoint = otherUsePoint;
	}

}
